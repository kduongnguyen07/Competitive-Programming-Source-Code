#include <bits/stdc++.h>
using namespace std;
const int maxn = 1e5 + 7;
int main ()
{
ios::sync_with_stdio(false);cin.tie(0);cout.tie(0);
    int n,s;
    cin >> n >> s;
    int a[maxn],d[maxn];
    for(int i = 1; i <= n; i++) cin >> a[i];
    int l = 1, res = INT_MAX;
    for(int r = 1; r <= n; r++) {
        for(int j = s; j >= a[r]; j--) {
            d[j] = max(d[j], d[j - a[r]]);
        }
        d[a[r]] = r;
        if(d[s] >= l) {
            res = min(res, r - d[s] + 1);
            l = d[s] + 1;
        }
    }
    if(res == INT_MAX) res = -1;
    cout << res;
return 0;
}
