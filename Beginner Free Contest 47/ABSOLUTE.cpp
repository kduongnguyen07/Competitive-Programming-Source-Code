#include <bits/stdc++.h>
using namespace std;
int abs (int val){
    if (val < 0) return -val;
    return val;
}
int main ()
{
ios::sync_with_stdio(false);cin.tie(0);cout.tie(0);
int N, Z, W;
    cin>>N>>Z>>W;
    vector<int> a (N+1,0);
    for (int i = 1; i <= N; i++){
        cin>>a[i];
    }
    int result = (N == 1) ? abs(a[1] - W) : max(abs(a[N] - W), abs(a[N-1] - a[N]));
    cout<<result<<endl;
return 0;
}
