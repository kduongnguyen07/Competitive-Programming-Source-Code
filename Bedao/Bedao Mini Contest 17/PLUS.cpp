#include<bits/stdc++.h>
#define vi vector<int>
#define vl vector<long long>
#define all(x) x.begin(), x.end()
#define ll long long
using namespace std;
int N, M, a[16][16], pref[16][16];

void pref2d() {
  for (int i = 1; i <= N; ++i) {
    for (int j = 1; j <= M; ++j) {
      pref[i][j] = pref[i - 1][j] + pref[i][j - 1] - pref[i - 1][j - 1] + a[i][j];
    }
  }
}

int calc(int x, int y, int p, int q) {
  if (p < x || q < y) return 0;
  return pref[p][q] - pref[x - 1][q] - pref[p][y - 1] + pref[x - 1][y - 1];
}

int main() {
#ifndef ONLINE_JUDGE
  freopen("input.txt", "r", stdin);
#endif
  cin >> N >> M;
  for (int i = 1; i <= N; ++i) {
    for (int j = 1; j <= M; ++j) {
      cin >> a[i][j];
    }
  }
  pref2d();

  int res = -2 * 1e9;
  for (int i = 1; i <= N; ++i) {
    for (int j = 1; j <= M; ++j) {
      for (int p = 1; p <= i; ++p) {
        for (int q = 1; q <= M; ++q) {
          if (p == i && q == j) continue;

          int ls11 = min( i - 1, min(N - i, min(j - 1, M - j)) );
          int ls22 = min( p - 1, min(N - p, min(q - 1, M - q)) );

          for (int ls1 = 0; ls1 <= ls11; ++ls1) {
            for (int ls2 = 0; ls2 <= ls22; ++ls2) {

              if (p == i) {
                if (q + ls2 >= j - ls1 && q < j) continue;
                if (q - ls2 <= j + ls1 && q > j) continue;
              }
              if (q == j) {
                if (p + ls2 >= i - ls1) continue;
              }
              if (p >= i - ls1) {
                if (q < j && q + ls2 >= j) continue;
                if (q - ls2 <= j &&  q > j) continue;
              }
              if (q < j && q >= j - ls1) {
                if (p + ls2 >= i) continue;
              }

              if (q <= j + ls1 && q > j) {
                if (p + ls2 >= i) continue;
              }

              int sum1 = pref[i + ls1][j + ls1] - calc(i + 1, j + 1, i + ls1, j + ls1)
              - calc(i - ls1, j + 1, i - 1, j + ls1) - calc(i - ls1, j - ls1, i - 1, j - 1)
              - calc(i + 1, j - ls1, i + ls1, j - 1);

              if (i - ls1 == 1) {
                if (j - ls1 > 1) sum1 -= pref[i + ls1][j - ls1 - 1];
              } else {
                sum1 -= pref[i + ls1][j - ls1 - 1];
                if (j - ls1 == 1) sum1 -= pref[i - ls1 - 1][j + ls1];
                else sum1 -= pref[i - ls1 - 1][j + ls1] - pref[i - ls1 - 1][j - ls1 - 1];
              }

              int sum2 = pref[p + ls2][q + ls2] - calc(p + 1, q + 1, p + ls2, q + ls2)
              - calc(p - ls2, q + 1, p - 1, q + ls2) - calc(p - ls2, q - ls2, p - 1, q - 1)
              - calc(p + 1, q - ls2, p + ls2, q - 1);

              if (p - ls2 == 1) {
                if (q - ls2 > 1) sum2 -= pref[p + ls2][q - ls2 - 1];
              } else {
                sum2 -= pref[p + ls2][q - ls2 - 1];
                if (q - ls2 == 1) sum2 -= pref[p - ls2 - 1][q + ls2];
                else sum2 -= pref[p - ls2 - 1][q + ls2] - pref[p - ls2 - 1][q - ls2 - 1];
              }

              res = max(res, sum1 + sum2);
            }
          }
        }
      }
    }
  }
  cout << res;
}
