#include <bits/stdc++.h>
using namespace std;

#define ll  long long
#define ld  double

#define sz(x)   (int)x.size()
#define all(x)  x.begin(), x.end()

#define pb  push_back
#define X   first
#define Y   second

const int   N   = 1e5 + 5;

typedef pair<int, int>  ii;

int main()  {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int n;  cin >> n;
    int K;  cin >> K;

    int sum = 0;
    ll  ans = 0;

    map<int, int> cnt[32];

    for(int i = 31 ; i >= 0 ; --i)
        cnt[i][0] = 1;
    for(int i = 0 ; i < n ; ++i)    {
        int x;  cin >> x;
        sum ^= x;

        for(int bit = 31 ; bit >= 0 ; --bit)    {
            if((K >> bit & 1) == 0)
                ans += cnt[bit][((sum ^ K) >> bit) ^ 1];

            cnt[bit][sum >> bit]++;
        }
    }
    cout << ans << endl;
}

