#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cin >> n;
    vector<long long> a(n+1), p(n+1);
    for(int i = 1; i <= n; i++) cin >> a[i];
    p[0] = 0;
    for(int i = 1; i <= n; i++) p[i] = a[i] + p[i-1];
    sort(p.begin(), p.end());
    long long ans = 0;
    long long cnt = 1;
    for (int i = 1; i <= n + 1; i++){
        if (i > n || p[i] != p[i - 1]){
            ans += (cnt*(cnt-1))/2;
            cnt = 1;
            continue;
        }
        cnt++;
    }
    cout << ans << endl;
    return 0;
}
