#include<bits/stdc++.h>
using namespace std;
#define ll long long
const int maxn = 2e3 + 7;
const int oo = 1e9 + 7;
const int MOD = 1e9 + 7;
const int block_size = 750;
int m, K;
ll a[maxn][maxn], ra[maxn][maxn], sa[maxn][maxn];
void makeS() {
    for(int i = 1; i < 2 * m; i++) {
        for(int j = 1; j < 2 * m; j++) {
            sa[i][j] = sa[i-1][j] + sa[i][j-1] - sa[i-1][j-1] + ra[i][j];
        }
    }
}
ll fa(int x, int y) {
    int i = x + m - y + K, j = x + y - 1 + K, l = 2 * K + 1;
    return sa[i][j] - sa[i-l][j] - sa[i][j-l] + sa[i-l][j-l];
}
bool check(int i, int j) {
    return i >= 1 && i <= m && j >= 1 && j <= m;
}
int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);
    cout.tie(NULL);
    cin >> m >> K;
    for(int i = 1; i <= m; i++) {
        for(int j = 1; j <= m; j++) {
            cin >> a[i][j];
            ra[i + m - j][i + j - 1] = a[i][j];
        }
    }

    makeS();
    ll res = 0;
    int ansi = -1, ansj = -1;
    for(int i = 1; i <= m; i++) {
        for(int j = 1; j <= m; j++) {
            if(i - K < 1 || i + K > m) continue;
            if(j - K < 1 || j + K > m) continue;

            ll ans = fa(i, j);
            if(ans > res) {
                ansi = i;
                ansj = j;
                res = ans;
            }
        }
    }
    if(!res) return cout << -1, 0;
    cout << ansi << ' ' <<ansj << endl;
    cout << res;
}
