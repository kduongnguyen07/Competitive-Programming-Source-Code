#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
#define maxN 200005
#define MOD 1000000007
pair <int,int> p[maxN];
int main () {
    int n;cin>>n;
    for (int i=1;i<=n;i++) {
            cin>>p[i].first;
            p[i].second = 0;
    }
    for (int i = n+1;i<=(2*n);i++) {
        cin>>p[i].first;
        p[i].second = 1;
    }
    sort(p+1, p+2*n+1);
    int cnt = 0, ans = 1;
    for (int i=1;i<=2*n;i++) {
        if (p[i].second == 0) {
            if (cnt < 0) ans = (1LL*ans*(-cnt))%MOD;
            ++cnt;
        } else {
            if (cnt > 0) ans = (1LL*ans*cnt)%MOD;
            --cnt;
        }
    }
    cout<<ans;
    return 0;
}
