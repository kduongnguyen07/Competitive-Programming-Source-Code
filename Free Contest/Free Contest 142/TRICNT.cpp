#include<bits/stdc++.h>
using namespace std;

#define ll long long
#define el '\n'
#define _ << ' '<<
#define memset( a, b ) memset( a, b, sizeof a )

template<typename X, typename Y> inline void amax( X &A, Y B ){ if(B>A) A=B; }
template<typename X, typename Y> inline void amin( X &A, Y B ){ if(B<A) A=B; }

const int maxn = 1e3+7, mod = 1e9+7, N = 1e5+7;

struct stick{
    ll len, cnt;

    bool operator < (const stick &other) const{
        return len < other.len;
    }
} arr[maxn];

ll bit[N];
void update(int pos, ll val){
    for(; pos<N; pos+=pos&(-pos)){
            (bit[pos] += val) %= mod;
    }
    return;
}
ll take(int pos){
    ll sum = 0;
    for(; pos; pos-=pos&(-pos)){
        sum += bit[pos];
    }
    return sum % mod;
}

ll cal(int l, int r){
    amin(r, N-1);
    if(l>r) return 0;

    ll res = take(r) - take(l-1);
    while(take<0) res+= mod;
    res %= mod;
    return res;
}

ll powup(ll base, ll mu){
    ll root = 1;
    while(mu){
        if(mu&1) (root *= base) %= mod;
        (base *= base) %= mod;
        mu >>= 1;
    }
    return root;
}

ll C(int k, ll n){
    ll res;
    if(k==2){
        res = (n*(n-1))%mod;
        (res *= powup(2, mod-2)) %= mod;
    } else {
        res = (n*(n-1))%mod;
        (res *= n-2) %= mod;
        (res *= powup(6, mod-2)) %= mod;
    }
    return res;
}

