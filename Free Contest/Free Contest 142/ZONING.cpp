#include <bits/stdc++.h>
#define ll long long
#define pl pair<ll,ll>
#define fi first
#define se second
#define giuncute ios_base::sync_with_stdio(0) , cin.tie(0);
using namespace std;
const ll maxn = 1e6 + 7;
ll n , x , a[maxn],t;
int main()
{
    giuncute;
    cin >> t;
    while(t--)
    {
        ll i = 1 , j = 1 , ans = 0 , sum = 0;
        cin >> n >> x;
        for (int i = 1 ; i <= n ; i++) cin >> a[i];
        while (i <= n)
        {
            sum += a[i];
            while (sum >= x)
            {
                if (sum == x) ans++;
                sum -= a[j];
                j++;
            }
            i++;
        }
        if (ans) cout << ans << "\n";
        else cout << "HUHU\n";
    }
}

