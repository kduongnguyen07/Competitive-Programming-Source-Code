#include <bits/stdc++.h>

using namespace std;

template<typename T>
T Maximum(T &a, T b)
{
	return (a = max(a, b));
}

using ll = long long;
const ll Mod = 1e9 + 7;
const ll inf = 2e9;
const int Sz = 301;

string s;
int pos[Sz][Sz];
bool visited[Sz][Sz][Sz];
int n;
int c0[Sz], c1[Sz];

void Find(int i, int Zeros, int Ones)
{
	if (Zeros < 0 || Ones < 0) return ;
	
	if (visited[i][Zeros][Ones] == true) return ;
	visited[i][Zeros][Ones] = true;
	Maximum(pos[Zeros + c0[i]][Ones + c1[i]], i); 

	if(Zeros + Ones > 1)
	{
		Find(i, Zeros, Ones - 1); // delete 1 from X
		Find(i, Zeros - 1, Ones); // delete 0 from X
	}

	if(i == n) return ;

	if(s[i] == '0')
	{
		Find(i + 1, Zeros + 1, Ones - 1); // delete 1 from X and insert s[i] into X
		// if(Zeros != 0) Find(i + 1, Zeros, Ones); // delete 0 from X and insert s[i] into X
	}else
	{
		Find(i + 1, Zeros - 1, Ones + 1); // delete 0 from X and insert s[i] into X
		// if(Ones != 0) Find(i + 1, Zeros, Ones); // delete 1 from X and insert s[i] into X
	}

	if(Zeros + Ones + (n - i) > 1) // delete first character
		Find(i + 1, Zeros, Ones);

	if((n - i) >= 2)
	{
		if(s[i] == '0' || s[i + 1] == '0')
			Find(i + 2, Zeros + 1, Ones);
		if(s[i] == '1' || s[i + 1] == '1')
			Find(i + 2, Zeros, Ones + 1);
	}
}

ll f[Sz][Sz][Sz];

ll dp(int i, int Zeros, int Ones)
{
	if(i == -1) return 0;
	if(Zeros < 0 || Ones < 0) return 0;
	if(i == n && Zeros + Ones == 0) return 1;
	ll &res = f[i][Zeros][Ones];
	if(res != -1) return res;
	res = 0;
	if(i == n) 
		res = (dp(i, Zeros - 1, Ones) + dp(i, Zeros, Ones - 1)) % Mod;
	else if(s[i] == '0')
		res = (dp(i + 1, Zeros, Ones) + dp(i, Zeros, Ones - 1)) % Mod;
	else res = (dp(i + 1, Zeros, Ones) + dp(i, Zeros - 1, Ones)) % Mod;
	return res;
}

void Prepare()
{

	n = s.size();

	for(int i = n - 1; i >= 0; i--)
	{
		c0[i] = c0[i + 1] + ('1' - s[i]);
		c1[i] = c1[i + 1] + (s[i] - '0');
	}
	memset(f, -1, sizeof f);
	memset(pos, -1, sizeof pos);
	Find(0, 0, 0);
}


signed main()
{
	
	cin >> s;
	Prepare();

	ll res = 0;
	for(int i = 0; i <= n; i++)
		for(int j = 0; j <= n; j++)
		{
			int k = pos[i][j];
			res = (res + dp(k, i - c0[k], j - c1[k])) % Mod;
		}
	cout << res << "\n";

	return 0; 
}
